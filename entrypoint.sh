#!/bin/bash
set -e

echo "
================================================================
Start run entrypoint
================================================================
"

echo "
================================================================
Start run composer install
================================================================
"
composer install

echo "
================================================================
Start run composer require livewire/livewire
================================================================
"
composer require livewire/livewire

echo "
================================================================
Start run npm install
================================================================
"
npm install

echo "
================================================================
Start run php artisan config:clear
================================================================
"
php artisan config:clear

echo "
================================================================
Start run php artisan config:cache
================================================================
"
php artisan config:cache

echo "
================================================================
Start run php artisan migrate:install
================================================================
"
php artisan migrate:install

echo "
================================================================
Start run npm run prod
================================================================
"
npm run prod

echo "
================================================================
Start run php artisan migrate
================================================================
"
php artisan migrate

echo "
================================================================
Start run php-fpm
================================================================
"
php-fpm

